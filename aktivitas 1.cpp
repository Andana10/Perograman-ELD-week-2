#include<iostream>
using namespace std;

int main(){
	int angka = 10; // Deklarasi beberapa angka
	int desimal = 5.5;
	int karakter = 'A';
	// Deklarasi beberapa variabel integer dalam satu baris
	int a = 1, b = 2, c=3;
	cout << "Nilai angka: " << angka << ::endl;
	cout << "Nilai desimal: " << desimal << ::endl;
	cout << "Nilai karakter: " << karakter << ::endl;
	cout << "Nilai a: " << a << ::endl;
	cout << "Nilai b: " << b << ::endl;
	cout << "Nilai c: " << c << ::endl;
	return 0;
}
